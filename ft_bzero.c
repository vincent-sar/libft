/*
** LIBRARY: <strings.h>
** SYNOPSIS: write zeroes to a byte string
**
** DESCRIPTION:
** 		The bzero() function writes n zeroed bytes to the string s. If n is
**	zero, bzero() does nothing.
*/

#include "libft.h"
#include <unistd.h>

void	bzero(void *s, size_t n)
{
	ft_memset(s, 0, n);
}
